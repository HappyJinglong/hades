package com.flyrish.hades.dto;

import java.util.List;

public class needsTable {
	private Integer jb;//届别
	private Integer num;//学生总人数
	private Integer grade;//年级
	private Integer semester;//上下学期
	private List<Integer> cpNum;//已完成学生数
	private List<Integer> cpLeve;//完成百分比
	public Integer getJb() {
		return jb;
	}
	public void setJb(Integer jb) {
		this.jb = jb;
	}
	public Integer getNum() {
		return num;
	}
	public void setNum(Integer num) {
		this.num = num;
	}
	public Integer getGrade() {
		return grade;
	}
	public void setGrade(Integer grade) {
		this.grade = grade;
	}
	public Integer getSemester() {
		return semester;
	}
	public void setSemester(Integer semester) {
		this.semester = semester;
	}
	public List<Integer> getCpNum() {
		return cpNum;
	}
	public void setCpNum(List<Integer> cpNum) {
		this.cpNum = cpNum;
	}
	public List<Integer> getCpLeve() {
		return cpLeve;
	}
	public void setCpLeve(List<Integer> cpLeve) {
		this.cpLeve = cpLeve;
	}
	
}
