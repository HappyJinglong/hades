package com.flyrish.hades.dto;

import java.io.Serializable;

public class UserSchoolDto implements Serializable{
	
	private Integer campusid;
	
	private String name;		//校区名称
	
	private String campusno;	//校区号
	
	private String schoolName;	//学校名称
	
	private Integer schoolId;//校区标识号
	
	
	
	
	
	public Integer getSchoolId() {
		return schoolId;
	}

	public void setSchoolId(Integer schoolId) {
		this.schoolId = schoolId;
	}

	public Integer getCampusid() {
		return campusid;
	}

	public void setCampusid(Integer campusid) {
		this.campusid = campusid;
	}

	public String getSchoolName() {
		return schoolName;
	}

	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}

	public String getCampusno() {
		return campusno;
	}

	public void setCampusno(String campusno) {
		this.campusno = campusno;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
