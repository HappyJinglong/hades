package com.flyrish.hades.dto;

import java.io.Serializable;

public class SpeekDto implements Serializable{
	
    /**
			 * 把同学的评价记录信息放入放入缓存中
			 * @param username 同学（评价人）的登录用户名
			 * @param columNum 栏目号
			 * @param proKey 评价记录的主键标识号
			 * @param columnName 栏目名称 如：合作与交流
			 * @param studentName 被评价学生的姓名 如：张三
			 * @param stuEduid 被评价学生的教育id
			 * @param date 评价的时间
			 */
	private String  username;
	private String  columNum;
	private String  proKey;
	private String  columnName;
	private String  studentName;
	private String  stuEduid;
	private String  appraisaltypeid;
	public String getAppraisaltypeid() {
		return appraisaltypeid;
	}
	public void setAppraisaltypeid(String appraisaltypeid) {
		this.appraisaltypeid = appraisaltypeid;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getColumNum() {
		return columNum;
	}
	public void setColumNum(String columNum) {
		this.columNum = columNum;
	}
	public String getProKey() {
		return proKey;
	}
	public void setProKey(String proKey) {
		this.proKey = proKey;
	}
	public String getColumnName() {
		return columnName;
	}
	public void setColumnName(String columnName) {
		this.columnName = columnName;
	}
	public String getStudentName() {
		return studentName;
	}
	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}
	public String getStuEduid() {
		return stuEduid;
	}
	public void setStuEduid(String stuEduid) {
		this.stuEduid = stuEduid;
	}
	
}
