package com.flyrish.hades.dto;

import java.io.Serializable;

/**
 * 年级班级设置菜单树
 * @author compaq
 *
 */
public class SchoolTreeDto implements Serializable{
	private String id;//学校ID，学生ID，或者是班级ID
	private String text;//显示名称，对应ID，为学校名称，学生名称或是班级名称
	private String compusId;//校区id
	private String edusyId;//学段id
	private String gradId; //年级id
	private String classId;//班级id
	private String edu_id;//学生教育id
	private String studentno;//学籍号
    private boolean leaf = false;
    private String name;
    private boolean expanded = false;
    private String key;
   
    public String getStudentno() {
		return studentno;
	}
	public void setStudentno(String studentno) {
		this.studentno = studentno;
	}
	public String getEdu_id() {
		return edu_id;
	}
	public void setEdu_id(String edu_id) {
		this.edu_id = edu_id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}

	/**
     * 菜单用
     */
    private String href;
    
    
    
	public String getGradId() {
		return gradId;
	}
	public void setGradId(String gradId) {
		this.gradId = gradId;
	}	
	
	public String getCompusId() {
		return compusId;
	}
	public void setCompusId(String compusId) {
		this.compusId = compusId;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	public String getEdusyId() {
		return edusyId;
	}
	public void setEdusyId(String edusyId) {
		this.edusyId = edusyId;
	}
	public String getClassId() {
		return classId;
	}
	public void setClassId(String classId) {
		this.classId = classId;
	}
    public boolean isLeaf()
    {
        return leaf;
    }

    public void setLeaf(boolean leaf)
    {
        this.leaf = leaf;
    }

	public String getHref() {
		return href;
	}

	public void setHref(String href) {
		this.href = href;
	}
	public boolean isExpanded() {
		return expanded;
	}
	public void setExpanded(boolean expanded) {
		this.expanded = expanded;
	}
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	
}
