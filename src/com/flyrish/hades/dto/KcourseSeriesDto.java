package com.flyrish.hades.dto;

import java.io.Serializable;

public class KcourseSeriesDto implements Serializable{

	private String series_id;//系列标号
	
	private String subject_id;//学科标号
	
	private String series_name;//系列名称

	public String getSeries_id() {
		return series_id;
	}

	public void setSeries_id(String series_id) {
		this.series_id = series_id;
	}

	public String getSubject_id() {
		return subject_id;
	}

	public void setSubject_id(String subject_id) {
		this.subject_id = subject_id;
	}

	public String getSeries_name() {
		return series_name;
	}

	public void setSeries_name(String series_name) {
		this.series_name = series_name;
	}

	@Override
	public String toString() {
		return "KcourseSeriesDto [series_id=" + series_id + ", subject_id="
				+ subject_id + ", series_name=" + series_name + "]";
	}
	
	
}
