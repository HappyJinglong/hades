package com.flyrish.hades.exception;

import org.acegisecurity.AuthenticationException;

public class UserSystemTypeException extends AuthenticationException {

	

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	/**
	 * 
	 * @param msg
	 */
	public UserSystemTypeException(String msg) {
		super(msg);
		
	}
	/**
	 * 
	 * @param msg
	 * @param t
	 */
	public UserSystemTypeException(String msg, Throwable t) {
		super(msg, t);
		
	}
	
}
