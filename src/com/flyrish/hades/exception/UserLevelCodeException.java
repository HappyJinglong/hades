package com.flyrish.hades.exception;

import org.acegisecurity.AuthenticationException;

public class UserLevelCodeException extends AuthenticationException {

	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	/**
	 * 
	 * @param msg
	 */
	public UserLevelCodeException(String msg) {
		super(msg);
	}

	/**
	 * 
	 * @param msg
	 * @param t
	 */
	public UserLevelCodeException(String msg, Throwable t) {
		super(msg, t);
	}
}
